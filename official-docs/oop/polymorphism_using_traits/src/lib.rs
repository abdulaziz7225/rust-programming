pub trait Draw {
    fn draw(&self);
}

// Trait objects allow for multiple concrete types to fill in for the trait object at runtime
pub struct Screen {
    pub components: Vec<Box<dyn Draw>>,
}

impl Screen {
    pub fn run(&self) {
        for component in self.components.iter() {
            component.draw();
        }
    }
}

